package com.cafe.wsRest.dto;

/**
 *
 * @author carlo
 */
public class VehiculoEnRutaDto {
    private String placa; 
    private String marca; 
    private String modelo;
    private String anio; 
    private String color;
    private String tipoVehiculo;
    private String estado;
    private String numeroCuenta;
    private int  parcialidad; 

    public VehiculoEnRutaDto() {
    }

    public VehiculoEnRutaDto(String placa, String marca, String modelo, String anio, String color, String tipoVehiculo, String estado, String numeroCuenta, int parcialidad) {
        this.placa = placa;
        this.marca = marca;
        this.modelo = modelo;
        this.anio = anio;
        this.color = color;
        this.tipoVehiculo = tipoVehiculo;
        this.estado = estado;
        this.numeroCuenta = numeroCuenta;
        this.parcialidad = parcialidad;
    }
    
    

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTipoVehiculo() {
        return tipoVehiculo;
    }

    public void setTipoVehiculo(String tipoVehiculo) {
        this.tipoVehiculo = tipoVehiculo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getNumeroCuenta() {
        return numeroCuenta;
    }

    public void setNumeroCuenta(String numeroCuenta) {
        this.numeroCuenta = numeroCuenta;
    }

    public int getParcialidad() {
        return parcialidad;
    }

    public void setParcialidad(int parcialidad) {
        this.parcialidad = parcialidad;
    }
    
    
}
